package com.example.bird_sound.localmodels;

class LocalModelDefinition {
    final String id;
    final String label;
    final String fileName;
    final String role;

    LocalModelDefinition(String id, String label, String fileName, String role) {
        this.id = id;
        this.label = label;
        this.fileName = fileName;
        this.role = role;
    }
}
